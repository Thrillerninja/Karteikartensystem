int userInput(){
    while (1) {
        char c = getch();   // get user input
        int value = 0;
        if (c >= '0' && c <= '3') {   // if input is a digit, add it to value
            value = value * 10 + (c - '0'); // converts to number
            return value;
        }
    }
}

